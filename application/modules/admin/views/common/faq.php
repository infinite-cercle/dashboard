              <div class="faq_question">What is Carbon Footprint and Carbon Credit?</div>
              <div class="faq_answer">
                The ultimate goal of carbon credits is to reduce the emission of greenhouse gases into the atmosphere. A carbon credit is a permit or certificate allowing the holder, such as a company, to emit carbon dioxide or other greenhouse gases. Each carbon credit values one ton of Carbon dioxide released. The United Nations' Intergovernmental Panel on Climate Change (IPCC) developed a carbon credit proposal as a market-oriented mechanism to slow worldwide carbon emissions. Countries or companies with surplus carbon credits can trade them with others who need more to meet their targets and avoid fines.
